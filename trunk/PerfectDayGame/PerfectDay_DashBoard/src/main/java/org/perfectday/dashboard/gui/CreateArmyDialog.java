private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
// TODO add your handling code here:
    
    double maxCost = Double.parseDouble(this.lMaxBattlePoint.getText());
    if(maxCost< this.model.getTotalCost())
        JOptionPane.showMessageDialog(this, "No se puede crear, el ejercito tiene demasiado coste","Aviso",JOptionPane.WARNING_MESSAGE);
    else{
        this.dispose();
    }
}//GEN-LAST:event_bAceptarActionPerformed

private void bAddMiniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddMiniActionPerformed
    if(this.cbLevel.getSelectedItem()!=null &&
            this.cbMinis.getSelectedItem()!=null){
        MiniDescription mini = (MiniDescription) this.cbMinis.getSelectedItem();
        MiniLevel level  = (MiniLevel) this.cbLevel.getSelectedItem();
        mini.setLevel(level);
        this.model.addRow(mini, level,mini.getCost() * MiniFactory.getInstance().getMiniCost(level));    
     //   this.armyTab.setModel(this.model);
        this.armyTab.updateUI();
        this.lTotalPoint.setText(nf.format(this.model.getTotalCost()));    
        this.pack();
    }
}//GEN-LAST:event_bAddMiniActionPerformed

private void cbMinisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbMinisActionPerformed
    if(this.cbLevel.getSelectedItem()!=null &&
            this.cbMinis.getSelectedItem()!=null){
        MiniDescription mini = (MiniDescription) this.cbMinis.getSelectedItem();
        MiniLevel level  = (MiniLevel) this.cbLevel.getSelectedItem();
        this.lPoint.setText(nf.format(mini.getCost() * MiniFactory.getInstance().getMiniCost(level)));
    }
}//GEN-LAST:event_cbMinisActionPerformed

