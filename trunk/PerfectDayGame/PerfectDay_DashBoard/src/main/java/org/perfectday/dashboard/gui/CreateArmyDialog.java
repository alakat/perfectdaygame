private void bDeleteMiniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteMiniActionPerformed
    int idx = this.armyTab.getSelectedRow();
    if(idx>-1){
        this.model.remove(idx);
        this.lTotalPoint.setText(nf.format(this.model.getTotalCost()));    
        this.pack();
    }
    this.armyTab.updateUI();
}//GEN-LAST:event_bDeleteMiniActionPerformed

    

