package org.perfectday.interfaz;

/**
 *
 * @author Miguel Angel Lopez Montellano (alakat@gmail.com)
 */
class Field3D {
    
    private int x;
    private int y;

    public Field3D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    
    
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    
}
